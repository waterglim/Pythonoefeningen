### Oefening 3
### Vraag de gebruiker om een getal in te voeren. 
### Print de multiplicatietafel van dat getal (van 1 tot 10). 
### Voorbeeld: 
### Input: 3 
### Output: 

i = input("Geef een getal mee om de tafel tot 10 te bekijken.")

for x in range (1,11):
    y = str(x)
    z = str(i)
    print(y + "x" + z +"= " + str(int(i)*int(x)))





