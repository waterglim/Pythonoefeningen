### oefening 1
### Schrijf een programma dat 
### het gemiddelde van drie getallen berekent. Vraag de gebruiker om de getallen in te voeren. 
### Voorbeeld:  
### Input: 5, 10, 15 
### Output: Het gemiddelde is: 10.0 

print("Gemiddelde berekening van 3 getallen : \n")
a = input("Gelieve het eerste getal te geven : ")
b = input("Gelieve het tweede getal te geven : ")
c = input ("Gelieve het laatste getal te geven : \n")

average = (float(a)+float(b)+float(c))/3

print("Het gemiddelde is van de drie getallen is: " + str(average))









